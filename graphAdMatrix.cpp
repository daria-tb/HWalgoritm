#include <iostream>
#include <vector>
using namespace std;

/* Граф — это нелинейная структура данных, состоящая из вершин и ребер. 
Вершины иногда также называют узлами, а ребра — это линии или дуги, 
соединяющие любые два узла в графе. 
Более формально граф состоит из набора вершин ( V ) и набора ребер ( E ). 
Граф обозначается как G(V, E) . 
Матрица смежности — это способ представления графа в виде матрицы булевых значений (0 и 1).*/


/* Представление неориентированного графа в видематрицы смежности:
На рисунке ниже показан неориентированный граф. 
Изначально вся матрица инициализируется значением 0. 
Если есть ребро от источника к месту назначения, 
мы вставляем 1 в оба случая ( adjMat[source][destination] 
и adjMat [ destination][source]), поскольку мы можем пойти в любом направлении. */
#include <iostream>
#include <vector>
using namespace std;

// Функція для додавання ребра між вершинами i та j у матриці суміжності
void addEdge(vector<vector<int>>& mat, int i, int j)
{
    mat[i][j] = 1;  // Позначаємо ребро i -> j
    mat[j][i] = 1;  // Позначаємо ребро j -> i (неорієнтований граф)
}

// Функція для виведення матриці суміжності на екран
void displayMatrix(vector<vector<int>>& mat)
{
    int V = mat.size();  // Кількість вершин
    for (int i = 0; i < V; i++)
    {
        for (int j = 0; j < V; j++)
            cout << mat[i][j] << " ";  // Виводимо значення матриці
        cout << endl;
    }
}

int main()
{
    int V = 4;  // Кількість вершин графа
    vector<vector<int>> mat(V, vector<int>(V, 0));  // Ініціалізація матриці суміжності нулями

    // Додаємо ребра між вершинами
    addEdge(mat, 0, 1);
    addEdge(mat, 0, 2);
    addEdge(mat, 1, 2);
    addEdge(mat, 2, 3);

    cout << "Adjacency Matrix Representation" << endl;
    displayMatrix(mat);  // Виводимо матрицю суміжності

    return 0;
}
